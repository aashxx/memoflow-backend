const mongoose = require('mongoose');

// Schema of the user signup and login to web app.
const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

const Users = mongoose.model("user",userSchema);
module.exports = Users